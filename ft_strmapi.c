#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*s1;

	if (!s || !f)
	{
		return (NULL);
	}
	s1 = (char *)malloc((ft_strlen(s) + 1) * sizeof(char));
	if (s1 == NULL)
	{
		return (NULL);
	}
	i = 0;
	while (s[i])
	{
		s1[i] = f(i, s[i]);
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
